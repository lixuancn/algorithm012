class Solution {
    /**
     * @param String[][] $board
     * @return Boolean
     */
    function isValidSudoku($board) {
        $rows = array();
        $cols = array();
        $block = array();
        foreach ($board as $i=>$row){
            foreach($row as $j=>$col){
                if($col == '.') continue;
                //数字 1-9 在每一行只能出现一次。
                $rows[$i][$col]++;
                //数字 1-9 在每一列只能出现一次。
                $cols[$j][$col]++;
                //数字 1-9 在每一个以粗实线分隔的 3x3 宫内只能出现一次。
                $index = intval($i / 3) * 3 + intval($j / 3);
                $block[$index][$col]++;
                if($rows[$i][$col] > 1 || $cols[$j][$col] > 1 || $block[$index][$col] > 1){
                    return false;
                }
            }
        }
        return true;
    }
}
